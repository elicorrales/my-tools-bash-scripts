#!/usr/bin/bash


#######################################################################
function mysql_menu {
#######################################################################
while [ 1 ];
do
	echo "|=============================================|"
	echo "|             MySQL Menu                      |"
	echo "|=============================================|"
	echo "|1) Start MySQL                               |"
	echo "|2) Stop  MySQL                               |"
	echo "|=============================================|"
	echo "|b) Back                                      |"
	echo "|e) Exit This Tool                            |"
	echo "|=============================================|"

	menu_choice="";
	read -p "Please make a selection:" menu_choice

	case $menu_choice in
		1)
			$(mysqld --console) &
			;;
		2)
			mysqladmin.exe -h 127.0.0.1 -u root shutdown
			;;
		b)
			break;
			;;
		e)
			exit 0;
			;;
		*)
			echo;echo;
			echo "Invalid selection: $menu_choice"
			echo;echo;
			;;
	esac
done

return 0;
#######################################################################
} # end function mysql_menu
#######################################################################

#######################################################################
function main_menu {
#######################################################################
while [ 1 ];
do
	echo "|=============================================|"
	echo "|              Main Menu                      |"
	echo "|=============================================|"
	echo "|1) Start/Stop MySQL                          |"
	echo "|=============================================|"
	echo "|e) Exit                                      |"
	echo "|=============================================|"

	menu_choice="";
	read -p "Please make a selection:" menu_choice

	case $menu_choice in

		1)
			mysql_menu
			;;
		e)
			break;
			;;
		*)
			echo;echo;
			echo "Invalid selection: $menu_choice"
			echo;echo;
			;;
	esac
done

return 0;

#######################################################################
} # end function main_menu
#######################################################################




#######################################################################
#######################################################################
#######################################################################
# MAIN MENU - start of script
#######################################################################
#######################################################################
#######################################################################

main_menu;

exit 0;
